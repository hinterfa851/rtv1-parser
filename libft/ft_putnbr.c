#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		ft_putchar('-');
		ft_putnbr(2);
		ft_putnbr(147483648);
	}
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			n *= -1;
		}
		if (n / 10 != 0)
		{
			ft_putnbr(n / 10);
			ft_putchar('0' + n % 10);
		}
		else
			ft_putchar('0' + n % 10);
	}
}
