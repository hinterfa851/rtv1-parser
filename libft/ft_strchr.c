#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (c == '\0')
	{
		while (*s)
			s++;
		return ((char *)s);
	}
	while (*s && *s != c)
		s++;
	if (*s)
		return ((char *)s);
	return (NULL);
}
